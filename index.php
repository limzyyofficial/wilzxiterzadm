  var merged = {};
  ORDERS.forEach(function(o) { merged[o.id] = o; });
  allOrders.forEach(function(o) { merged[o.id] = o; });
  var list = Object.values(merged).sort(function(a,b){ return (b.timestamp||0)-(a.timestamp||0); });
  /* Simpan ulang ke lokal */
  ORDERS = list.filter(function(o){
